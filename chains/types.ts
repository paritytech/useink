export type Account = '*25519' | 'secp256k1' | 'Sr25519';

export type JsonString = string;

export interface Token {
  symbol: string;
  decimals: number;
  ed: string;
  onChainId: JsonString | number;
  coingeckoId?: string;
}

export interface TokenAsset {
  assetId: string | number;
  symbol: string;
  coingeckoId?: string;
}

export type Chain = {
  id: ChainId;
  name: string;
  account: Account;
  subscanUrl?: string;
  overrideNativeTokenId?: string;
  chainspecQrUrl?: string;
  latestMetadataQrUrl?: string;
  rpcs: string[];
  coingeckoId?: string | null;
  paraId?: number;
  relay?: {
    id: string;
  };
  balanceModuleConfigs?: {
    [k: string]: { disable?: boolean; tokens?: (Token | TokenAsset)[] };
  };
};

